using System;

class PrintNumbers
{
    static void Main()
    {
        Console.WriteLine("1");
        Console.WriteLine("101");
        Console.WriteLine("1001");

        string s = "1" +
        "\r\n" +
        "101" +
        "\r\n" +
        "1001";
        Console.Write(s);
        Console.Read();
    }
}

